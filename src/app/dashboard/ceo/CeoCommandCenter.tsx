function TaskCard({ task, onStatusChange, onEdit, compact = false }: {
  task: Task; onStatusChange: (id: string, status: string) => void
  onEdit?: (t: Task) => void; compact?: boolean
}) {
  const cat  = CATEGORY_CONFIG[task.category]
  const pri  = PRIORITY_CONFIG[task.priority]
  const CatIcon = cat.icon
  const [open, setOpen] = useState(false)

  const nextStatus: Record<string, string | null> = { TODO: 'DOING', DOING: 'DONE', DONE: null }
  const prevStatus: Record<string, string | null> = { DOING: 'TODO', DONE: 'DOING', TODO: null }

  return (
    <div className={`rounded-xl border ${task.status === 'DONE' ? 'border-zinc-100 dark:border-zinc-800 opacity-60' : cat.border} bg-white dark:bg-ads-dark-card p-3.5 transition-all hover:shadow-sm`}>
      <div className="flex items-start gap-2.5">
        {/* Score Badge */}
        <div className={`shrink-0 w-10 h-10 rounded-lg flex flex-col items-center justify-center text-center ${task.priority === 'CRITICAL' ? 'bg-red-100 dark:bg-red-950/30' : task.priority === 'HIGH' ? 'bg-orange-100' : 'bg-zinc-50 dark:bg-zinc-800'}`}>
          <span className={`text-base font-black leading-none ${pri.color}`}>{task.priorityScore}</span>
          <span className="text-[8px] text-zinc-400 uppercase">score</span>
        </div>

        <div className="flex-1 min-w-0">
          <div className="flex items-center gap-1.5 flex-wrap mb-0.5">
            <span className={`px-1.5 py-0.5 rounded text-[10px] font-bold ${cat.badge} flex items-center gap-0.5`}>
              <CatIcon className="w-2.5 h-2.5" />{cat.label}
            </span>
            <span className={`px-1.5 py-0.5 rounded text-[10px] font-bold ${pri.badge}`}>{pri.label}</span>
            {task.autoGenerated && <span className="px-1.5 py-0.5 rounded text-[10px] bg-zinc-100 text-zinc-500">⚡ Auto</span>}
          </div>
          <p className={`text-sm font-semibold leading-snug ${task.status === 'DONE' ? 'line-through text-zinc-400' : ''}`}>{task.title}</p>
          {!compact && task.description && (
            <button onClick={() => setOpen((v) => !v)} className="text-[11px] text-zinc-400 hover:text-zinc-600 flex items-center gap-0.5 mt-0.5">
              {open ? <><ChevronUp className="w-3 h-3" />Ocultar</> : <><ChevronDown className="w-3 h-3" />Ver mais</>}
            </button>
          )}
          {open && <p className="text-xs text-zinc-500 mt-1 leading-relaxed">{task.description}</p>}
          {task.revenueImpact && (
            <p className="text-[11px] text-green-600 font-semibold mt-1">💰 Impacto estimado: {brlFull(task.revenueImpact)}</p>
          )}
        </div>

        {/* Controles */}
        <div className="flex flex-col gap-1 shrink-0">
          {prevStatus[task.status] && (
            <button onClick={() => onStatusChange(task.id, prevStatus[task.status]!)}
              className="p-1 rounded hover:bg-zinc-100 dark:hover:bg-zinc-800 transition-colors" title="Voltar">
              <ArrowUp className="w-3.5 h-3.5 text-zinc-400" />
            </button>
          )}
          {nextStatus[task.status] && (
            <button onClick={() => onStatusChange(task.id, nextStatus[task.status]!)}
              className="p-1 rounded hover:bg-green-50 dark:hover:bg-green-950/20 transition-colors" title="Avançar">
              <ArrowDown className="w-3.5 h-3.5 text-green-500" />
            </button>
          )}
          {onEdit && task.status !== 'DONE' && (
            <button onClick={() => onEdit(task)} className="p-1 rounded hover:bg-zinc-100 dark:hover:bg-zinc-800 transition-colors">
              <Pencil className="w-3.5 h-3.5 text-zinc-400" />
            </button>
          )}
        </div>
      </div>
    </div>
  )
}

