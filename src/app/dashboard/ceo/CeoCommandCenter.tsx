type Task = {
  id: string; title: string; description: string | null
  category: 'ESCALA' | 'EFICIENCIA' | 'INFRA' | 'GESTAO'
  impact: number; urgency: number; priorityScore: number
  priority: 'CRITICAL' | 'HIGH' | 'MEDIUM' | 'LOW'
  status: 'TODO' | 'DOING' | 'DONE' | 'ARCHIVED'
  revenueImpact: number | null; dueDate: string | null
  completedAt: string | null; autoGenerated: boolean
  createdAt: string
}

type Stats = { byStatus: Record<string,number>; byCategory: Record<string,number>; escalaCompletedPct: number; escalaAlert: boolean }

type Insights = {
  period: { year: number; month: number; daysLeft: number; dailyNeed: number }
  revenue: { realized: number; goal: number; pct: number; grossMargin: number; marginPct: number; salesCount: number; onTrack: boolean }
  burnRate: { spent: number; limit: number; pct: number; alert: boolean; remaining: number }
  cac: { value: number; alert: boolean; status: string; limit: number }
  monthlyRevenue: { month: string; revenue: number; margin: number }[]
  taskStats: Record<string, number>
  alerts: { messages: string[] }
}

