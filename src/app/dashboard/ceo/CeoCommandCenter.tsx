function TaskForm({ onSave, onCancel, initial }: {
  onSave: (d: Partial<Task>) => Promise<void>
  onCancel: () => void
  initial?: Partial<Task>
}) {
  const [title, setTitle]           = useState(initial?.title ?? '')
  const [description, setDesc]      = useState(initial?.description ?? '')
  const [category, setCategory]     = useState(initial?.category ?? 'ESCALA')
  const [impact, setImpact]         = useState(initial?.impact ?? 5)
  const [urgency, setUrgency]       = useState(initial?.urgency ?? 5)
  const [revenueImpact, setRevImp]  = useState(initial?.revenueImpact ? String(initial.revenueImpact) : '')
  const [saving, setSaving]         = useState(false)
  const score = impact * 2 + urgency

  const submit = async (e: React.FormEvent) => {
    e.preventDefault(); setSaving(true)
    await onSave({ title, description: description || undefined, category, impact, urgency, revenueImpact: revenueImpact ? parseFloat(revenueImpact) : undefined })
    setSaving(false)
  }

  return (
    <form onSubmit={submit} className="rounded-2xl border border-primary-200 bg-primary-50 dark:bg-primary-950/10 p-4 space-y-3">
      <div className="flex items-center justify-between">
        <h3 className="font-bold text-sm">{initial ? 'Editar Tarefa' : '+ Nova Tarefa'}</h3>
        <button type="button" onClick={onCancel}><X className="w-4 h-4 text-zinc-400" /></button>
      </div>

      <input required value={title} onChange={(e) => setTitle(e.target.value)} placeholder="Título da tarefa..." className="input-field" />
      <textarea rows={2} value={description} onChange={(e) => setDesc(e.target.value)} placeholder="Contexto e próximos passos..." className="input-field resize-none" />

      <div className="grid grid-cols-2 gap-3">
        <div>
          <label className="block text-xs font-semibold mb-1">Categoria</label>
          <select value={category} onChange={(e) => setCategory(e.target.value as typeof category)} className="input-field">
            {Object.entries(CATEGORY_CONFIG).map(([k, v]) => <option key={k} value={k}>{v.label}</option>)}
          </select>
        </div>
        <div>
          <label className="block text-xs font-semibold mb-1">Impacto Financeiro Estimado</label>
          <input type="number" value={revenueImpact} onChange={(e) => setRevImp(e.target.value)} className="input-field" placeholder="Ex: 50000" />
        </div>
      </div>

      {/* Sliders de Impacto × Urgência */}
      <div className="grid grid-cols-2 gap-3">
        <div>
          <label className="block text-xs font-semibold mb-1">Impacto no Faturamento: <span className="text-primary-600 font-black">{impact}</span>/10</label>
          <input type="range" min={1} max={10} value={impact} onChange={(e) => setImpact(parseInt(e.target.value))}
            className="w-full accent-primary-600" />
        </div>
        <div>
          <label className="block text-xs font-semibold mb-1">Urgência: <span className="text-primary-600 font-black">{urgency}</span>/10</label>
          <input type="range" min={1} max={10} value={urgency} onChange={(e) => setUrgency(parseInt(e.target.value))}
            className="w-full accent-primary-600" />
        </div>
      </div>

      <div className="flex items-center justify-between">
        <div className={`px-3 py-1.5 rounded-lg text-sm font-bold ${score >= 25 ? 'bg-red-100 text-red-700' : score >= 18 ? 'bg-orange-100 text-orange-700' : 'bg-zinc-100 text-zinc-600'}`}>
          Score: {score} — {score >= 25 ? 'CRÍTICA 🚨' : score >= 18 ? 'ALTA 🔥' : score >= 12 ? 'MÉDIA' : 'BAIXA'}
        </div>
        <div className="flex gap-2">
          <button type="button" onClick={onCancel} className="btn-secondary text-sm">Cancelar</button>
          <button type="submit" disabled={saving || !title.trim()} className="btn-primary text-sm flex items-center gap-1.5">
            {saving ? <Loader2 className="w-3.5 h-3.5 animate-spin" /> : <CheckCircle2 className="w-3.5 h-3.5" />}
            Salvar
          </button>
        </div>
      </div>
    </form>
  )
}

