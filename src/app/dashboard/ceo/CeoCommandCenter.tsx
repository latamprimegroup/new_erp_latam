const brl = (v: number) => v.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL', maximumFractionDigits: 0 })
const brlFull = (v: number) => v.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })

// ─────────────────────────────────────────────────────────────────────────────
// Barra de progresso
// ─────────────────────────────────────────────────────────────────────────────

function ProgressBar({ pct, color = 'bg-primary-600', height = 'h-3' }: { pct: number; color?: string; height?: string }) {
  return (
    <div className={`w-full ${height} rounded-full bg-zinc-100 dark:bg-zinc-800 overflow-hidden`}>
      <div className={`${height} rounded-full transition-all duration-700 ${color}`} style={{ width: `${Math.min(100, pct)}%` }} />
    </div>
  )
}

// ─────────────────────────────────────────────────────────────────────────────
// Card de KPI
// ─────────────────────────────────────────────────────────────────────────────

function KpiCard({ label, value, sub, pct, pctColor, alert }: {
  label: string; value: string; sub?: string; pct?: number; pctColor?: string; alert?: boolean
}) {
  return (
    <div className={`rounded-2xl border p-4 ${alert ? 'border-red-300 bg-red-50 dark:bg-red-950/10' : 'border-zinc-200 dark:border-zinc-700 bg-white dark:bg-ads-dark-card'}`}>
      <p className="text-xs font-semibold text-zinc-500 uppercase mb-1">{label}</p>
      <p className={`text-2xl font-bold ${alert ? 'text-red-600' : 'text-zinc-900 dark:text-white'}`}>{value}</p>
      {sub && <p className={`text-xs mt-0.5 ${alert ? 'text-red-500' : 'text-zinc-400'}`}>{sub}</p>}
      {pct !== undefined && <div className="mt-2"><ProgressBar pct={pct} color={pctColor} /></div>}
    </div>
  )
}

